def clean_output(text):
    """Remove markdown formatting for clean terminal display."""
    text = text.replace('**', '')
    text = text.replace('*', '')
    return text


def convert_etl_to_text(etl_file):
    """Convert Windows ETL (Event Trace Log) file to readable text format."""
    print(f"\n🔄 Detected ETL file - converting to text format...")
    
    # Create temporary text file path
    temp_txt_file = etl_file.replace('.etl', '_converted.txt')
    
    try:
        # Use Windows tracerpt utility to convert ETL to text
        command = f'tracerpt "{etl_file}" -o "{temp_txt_file}" -of CSV -summary "{temp_txt_file}.summary"'
        
        print(f"⏳ Converting... (this may take 10-30 seconds)")
        
        result = subprocess.run(
            command,
            shell=True,
            capture_output=True,
            text=True,
            timeout=60
        )
        
        if result.returncode == 0 and os.path.exists(temp_txt_file):
            print(f"✅ Conversion successful!")
            print(f"📄 Converted file: {temp_txt_file}")
            return temp_txt_file
        else:
            print(f"❌ Conversion failed: {result.stderr}")
            return None
            
    except subprocess.TimeoutExpired:
        print("❌ Conversion timed out (file too large)")
        return None
    except Exception as e:
        print(f"❌ Conversion error: {str(e)}")
        return None


