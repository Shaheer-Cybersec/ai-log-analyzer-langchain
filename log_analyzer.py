    print("\n" + "=" * 70)
    print("📁 LOG FILE SELECTION")
    print("=" * 70)
    print(f"\nDefault sample: {default_sample}")
    print("\nOptions:")
    print("  1. Press ENTER to use sample log (demo)")
    print("  2. Type/paste your log file path")
    print("  3. Drag & drop a file into this window")
    print("\n💡 Supports: .log, .txt, .etl (auto-converts ETL files)\n")
    
    user_input = input("Enter log file path (or press Enter for sample): ").strip()
    user_input = user_input.strip('"').strip("'")
    
    if user_input == "":
        if os.path.exists(default_sample):
            print(f"\n✅ Using sample log: {default_sample}")
            return default_sample
        else:
            print(f"\n⚠️  Sample log not found: {default_sample}")
            return None
    else:
        if os.path.exists(user_input):
            print(f"\n✅ Using log file: {user_input}")
            return user_input
        else:
            print(f"\n❌ File not found: {user_input}")
            return None


